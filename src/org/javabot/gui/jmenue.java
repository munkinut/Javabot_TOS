  private void conDisconButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conDisconButtonActionPerformed
      if (jba.isConnected()) {
          jba.disconnect();
          conDisconButton.setToolTipText("Click to Connect");
          javaBotSettingsMenuItem.setEnabled(true);
          disconnectMenuItem.setEnabled(false);
          connectMenuItem.setEnabled(true);
      }
      else {
          jba.connect();
          conDisconButton.setToolTipText("Click to Disconnect");
          javaBotSettingsMenuItem.setEnabled(false);
          disconnectMenuItem.setEnabled(true);
          connectMenuItem.setEnabled(false);
      }
  }//GEN-LAST:event_conDisconButtonActionPerformed

